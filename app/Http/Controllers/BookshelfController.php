    public function index(): View
    {
        $currently_reading = self::$currently_reading;
        $finished_books = collect(self::$finished_books)->sortBy(callback: '3', descending: true);
        $number_of_books = count($finished_books);
        $number_of_pages = $finished_books->sum('4');
        $finished_by_year = collect(self::$finished_books)
            ->groupBy(function (array $book): int {
                return (int) Carbon::createFromFormat('Y-m-d', $book[3])?->format('Y');
            })
            ->map(function ($group) {
                return [
                    'count' => $group->count(),
                    'pages' => $group->sum(fn(array $b) => $b[4]),
                ];
            })
            ->sortKeysDesc()
            ->toArray();
        $to_be_read = collect(self::$to_be_read);
        $number_to_be_read = $to_be_read->count();
        $number_of_to_be_read_pages = $to_be_read->sum('4');

        $this->calculateStatsFor2025();
        $stats = $this->stats;
        return view('bookshelf', compact('finished_books', 'currently_reading', 'number_of_books', 'number_of_pages', 'to_be_read', 'finished_by_year', 'number_to_be_read', 'number_of_to_be_read_pages', 'stats'));
    }

    private function calculateStatsFor2025(): void
    {
        $logs_2025 = collect(self::$reading_logs_2025)
            ->groupBy(function (array $book): int {
                return (int) Carbon::createFromFormat('Y-m-d', $book[1])?->format('md');
            })
            ->map(function ($group) {
                $pages = $group->sum(fn(array $b) => $b[2]);
                self::calculatePageAward($pages, '2025');
                return [
                    $pages,
                ];
            });

        $this->stats['2025']['days'] = count($logs_2025);
        $this->stats['2025']['percentage'] = intval(number_format(($this->stats['2025']['days'] / Carbon::now()->daysInYear * 100)));
    }

    private function calculatePageAward(int $pages, string $year): void
    {
        if ($pages < 20) {
            return;
        } elseif ($pages <= 24) {
            $this->stats[$year]['minimum'] += 1;
        } elseif ($pages < 50) {
            $this->stats[$year]['bronze'] += 1;
        } elseif ($pages < 100) {
            $this->stats[$year]['silver'] += 1;
        } elseif ($pages < 200) {
            $this->stats[$year]['gold'] += 1;
        } else {
            $this->stats[$year]['platinum'] += 1;
        }
    }

